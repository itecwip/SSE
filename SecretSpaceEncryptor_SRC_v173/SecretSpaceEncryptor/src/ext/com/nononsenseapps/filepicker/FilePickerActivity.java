package ext.com.nononsenseapps.filepicker;

import android.annotation.SuppressLint;

import java.io.File;

@SuppressLint("Registered")
public class FilePickerActivity extends AbstractFilePickerActivity<File> {

    public FilePickerActivity() {
        super();
    }

    @Override
    protected AbstractFilePickerFragment<File> getFragment(
            final String startPath, final int mode, final boolean allowMultiple,
            final boolean allowCreateDir, final String extensionsFilter) {
        AbstractFilePickerFragment fragment = new FilePickerFragment();
        fragment.setArgs(startPath, mode, allowMultiple, allowCreateDir, extensionsFilter);
        return fragment;
    }
}
